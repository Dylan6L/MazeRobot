def SolveMaze(mazeInput):
    maze = mazeInput
    nums = queue.Queue()
    nums.put("")
    add = ""

    while not findEnd(maze, add): 
        add = nums.get()
        #print(add)
        for j in ["L", "R", "U", "D"]:
            put = add + j
            if valid(maze, put):
                nums.put(put)

    return solution